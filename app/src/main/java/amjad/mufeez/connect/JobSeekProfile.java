package amjad.mufeez.connect;

public class JobSeekProfile {
    String name;
    String email;
    String phone;
    String linkedin;
    String github;
    String industry;
    String devpost;

    public JobSeekProfile(String name, String email, String phone, String linkedin, String github, String industry, String devpost) {
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.linkedin = linkedin;
        this.github = github;
        this.industry = industry;
        this.devpost = devpost;

    }
}
